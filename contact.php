      <h2>More info of Architecture</h2>
      <section>
        <article>
          <h3>Blockchain</h3>
          <p>
            We can say Blockchain is one kind of database. But the largest difference is there is no central administrator. It means we can deal with data without thirdparty. 
          </p>
          <p>
            Comparing to conventional database, it is much difficult to cracking. Because every devices has the same data respectively. We can easily found ilegal canged data by comparing to data in some device to another. That's why, this tecnology is good for dealing with currency.
          </p>
        </article>
        <article>
          <h3>Mining</h3>
          <p>
            Mining is getting cryptocurrency as reward.
          </p>
          <p>
            Every fixed period, cryptocurrency appends a history of transaction. But because of the amount of data, it has to be helped by someone. 
          </p>
          <p>
            When we finish to append, we can take cryptocurrency. At this moment, new crypt was issued. This is called "Mining". In other words, if there was no one to help this task, that kind of cryptocurrency get broken. 
          </p>
        </article>
        <article>
          <h3>Node</h3>
          <p>
            As main page mentioned, node is a devece that is joining in the network of cryptocurrency. 
          </p>
          <p>
            In the system, node has a huge role. Each node is monitoring each other. If there were some difference between one and another, there may be some cracking.
          </p>
          <p>
            It follows that all of devices that dealt with specific cryptocurrency have all of history of transactions. As we can see, it is very difficult to cracking. If you want to, you have to falsify the data in all of devices at once.
          </p>
        </article>
